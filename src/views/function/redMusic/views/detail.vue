            <h3>乔羽 （北京大学歌剧研究院名誉院长）</h3>
            <p>
              乔羽，1927年11月16日出生于山东省济宁市，词作家、剧作家。代表作有《让我们荡起双桨》《我的祖国》《人说山西好风光》《刘三姐》《难忘今宵》，现任北京大学歌剧研究院名誉院长。 [1] 
幼时家庭生活拮据，靠哥哥做店员维持生活。高中期间当过小学教员。1946年初入晋冀鲁豫边区北方大学学习，开始在报刊发表诗歌和小说，还写过秧歌剧。1948年华北联合大学与北方大学合并为华北大学，调入华大三部创作室，开始专业创作。 [1] 
2010年4月9日，担任北京大学歌剧研究院名誉院长。曾担任中国歌剧舞剧院院长、中国音乐文学学会主席、中国社会音乐研究会名誉会长、第八届全国政协委员。中国文学艺术界联合会第十届荣誉委员。
