          <h3>作词过程</h3>
          <p>
            长春电影制片厂拍摄完《上甘岭》之后，导演沙蒙找到乔羽要其为主题曲作词，乔羽接到邀请当夜，登车由南昌赶往上海，由上影厂厂长袁文殊安排车次赶到长春。沙蒙、乔羽会面后，沙蒙便把情况和盘摆给了乔羽：《上甘岭》影片已经拍完，样片也剪出来了。只留下安排插曲的那几分钟戏，等歌出来后补拍。全剧组每天的花销巨大。因此，沙蒙要乔羽快速创作，并要求这首歌能够经久不衰。
            [6]
            乔羽在作词时想走一个不同于以往的写作路子，可又想不出来，而沙蒙几乎每天都到他屋子里来催稿，乔羽也没办法。直到他想起他在江西看到长江时的场景，才把歌词写了出来。沙蒙拿着稿子看了半个小时后，询问第一句为何不用万里长江或长江万里，乔羽认为这样写可能会让那些不在长江边上的人从心理上产生距离，失去亲切感，最终沙蒙认可了乔羽的想法。
          </p>
          <h3>作曲过程</h3>
          <p>
            1956年，沙蒙请刘炽为《我的祖国》作曲。刘炽为了让人们喜爱这首歌，调查研究了1949年至1955年人们喜欢唱的歌曲，从中选出十首歌曲反复倾听，最后从其中一首《卢沟问答》中的第一句找到了《我的祖国》开始的半句。刘炽在长春电影制片厂的小白楼创作时，为了避免外界的干扰，在门上贴了“刘炽死了”的条子
          </p>
          <h3>挑选歌手</h3>
          <p>
            为找适合演唱《我的祖国》的歌手，长影请了一批中国内地擅唱民歌的歌唱家试唱，结果都不太满意。后来，乔羽提出请郭兰英来唱，郭兰英试唱后，长影便选定她作为歌曲的演唱者。歌曲的录音是在中央人民广播电台进行的，录制完成后的第二天电台便向全中国播放了这支歌。
