          <span class="h2">主要影响</span>
          <span class="next"></span>
        </div>
        <p class="live">
          光绪二十九年（1903年）5月，一本署名“革命军中马前卒邹容”的书——《革命军》由上海大同书局正式出版。在这本书中，邹容开宗明义地提出，要用革命的手段推翻清朝的皇权，建立资产阶级民主国家，并为这个国家定名“中华共和国”。《革命军》为两千多年的封建专制制度敲响了丧钟，为资产阶级民主革命吹响了号角，成为一篇名副其实的反帝、反封建的战斗檄文。
        </p>
        <p>
          邹容所撰《革命军》风行国内和海外华侨中，销售达110万册，对鼓动清末革命高潮产生了难以估量的作用。孙中山看到《革命军》后，赞赏不已，认为“此书感动皆捷，其功效真不可胜量”。他对邹容和章太炎身陷牢狱，异常关切。二十九年初冬，他又到檀香山，改组兴中会，建立“中华革命军”，确定“今后同志当自称为军，所以记邹容之功也”。
          [10]
          迄1917—1919年期间，孙中山著《建国方略》，还言之谆谆：“《革命军》一书，为排满最激烈之言论，华侨极为欢迎；其开导华侨风气，为力者大。”
          [11]
        </p>
