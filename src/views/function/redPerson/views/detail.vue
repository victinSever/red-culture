            <h3>人物墓地</h3>
            <p>
              邹容于1905年4月3日死于狱中，年仅21岁。其遗体由《中外日报》馆收殓后葬于华泾。辛亥革命胜利后，孙中山就任临时大总统，追授邹容为陆军大将军衔。后由华泾刘季平（刘三）移柩于华泾镇黄叶楼西侧（今华泾路中心河桥桥堍右侧200米处，近位育路口）。墓碑由蔡元培所题，为避清廷耳目，写为“周容之墓”。墓面南为圆形石砌墓，高2.36米，直径2.48米，墓前尖顶青石刻书“邹容之墓”。过神道石阶有石碑一座，刻有章太炎篆书“赠大将军巴县邹容”。
            </p>
            <p>
              墓后为章太炎撰文、于右任书的青石墓表。碑后复有造型美观的高大石屏，气势雄伟，东西两边各立一座石亭。在“文革”中被毁。1980年，为纪念辛亥革命70周年，上海市文物管理委员会重修邹容墓。墓区总占地一亩余，墓台坐北朝南，面向大道，四周环有长青松柏，庄严肃穆，两边是正方形绿化区，广植花草。邹容墓现已整修一新，建成花园式陵园，向公众免费开放。
            </p>
          </div>
          <div>
            <h3>地名纪念</h3>
            <p>
              为纪念邹容，在出生地重庆和就义地上海都有以他的名字命名的邹容路。1939年5月3日、4日接连两天，日本飞机对重庆进行大轰炸，把重庆城区的繁华街道几乎全部炸毁，居民伤亡六七千人，史称“五三五四大轰炸”。在这场大轰炸中，从大梁子（现新华路）中央公园（现人民公园）路口到临江门的5条繁华街道———三王庙、苍坪街、天官街、柴家巷、夫子池全部被毁。因为这5条街道首尾相接，又是重庆最重要的商业区，后来重建，就尽可能扩宽修直，并将其合并。因此路是重建的，遂取名新生路。
            </p>
            <p>
              1943年12月，当时的市政府为纪念邹容，将新生路改名为邹容路。如今的邹容路从公园路口起，由南向北一直延伸到临江门。解放碑就耸立在邹容路和民族路、民权路交会处的街心处。
            </p>
