          <div>
            <h3>少年时期</h3>
            <p>
              邹容生于重庆府巴县（今重庆市渝中区）一个商业资本家家庭，祖籍湖北麻城。光绪十七年（1891年），邹容随大哥蕴丹入私塾发蒙。十一二岁的时候，他就已熟读四书五经，能诵读《史记》、《汉书》等史学名著。此时，正当维新运动高涨之时，邹容逐渐接触到以推介“新学”、“西学”为主的书刊。他于是开始接受另一种启蒙教育，并因此而在一定程度上萌发了蔑视封建文化、科举制度的感性认识，觉察到清朝统治的腐败，对中国面临的遭受帝国主义瓜分中国狂潮的险境也深感忧虑。这样，邹容的思想和志趣，就完全和父亲的期待相左，新旧冲突，很快就在父子之间爆发。其父要他科举高中，他却讨厌经学的陈腐，鄙弃八股功名，喜读《天演论》、《时务报》等新学书刊，心向维新变革的新思潮。
              光绪二十三年（1897年），邹容满12岁，奉父命偕大哥应巴县童子试。刚进头场，就因为试题很生僻，他当即同主考官顶撞起来，于是愤而罢考离去。回到家里，他遭到了父亲的责打。但邹容并不服气，倔强地向父亲申辩说：“臭八股儿不愿学，满场儿不愿入。衰世功名，得之又有何用？”
              [1]
              事后，其父邹子璠虽迫令儿子进入重庆经学书院继续接受儒学经典的教育，但邹容仍不就范，在书院“指天画地，非尧舜，薄周孔，无所避”。
              [2] “攻击程朱及清儒学说，尤体无完肤”。 [3] 因此，被书院开除。
              光绪二十四年（1898年），戊戌变法失败，当得知谭嗣同等六君子变法遇难的消息，他悲愤不已，作诗曰：“赫赫谭君故，湘湖士气衰。惟冀后来者，继起志勿灰。”
              [4]
              以表达了他的惋惜与变革志向。这时，邹容还算不上成年人。后入重庆经学院读书，仍关心国家大事，立志救国救民，常侃侃议论政事，以致被除名。
              [5]
            </p>
          </div>
          <div>
            <h3>东渡求学</h3>
            <p>
              光绪二十七年（1901），四川省首次选派学生赴日留学，在成都举办考试。邹容要求前往应考。邹子璠感到无法使儿子流连在科举的道路上，于是允诺他去一试，其目的在于让他留学回来获得朝廷赐予举人、进士的功名，仍旧跻入官场。因为，清朝业已下诏“变法”，盛传已有授予留学生功名官职的措施。是年六月，邹容冒着盛暑从重庆步行到成都参加考试。试后获录取，由主持此次考试的候补知府李立元领着晋见四川总督奎俊。奎俊“勉励数语，旋命归渝治行装，于八月中旬同往日本”。
              [2]
              可是，就在临行之前，邹容被某些顽固分子诬为“聪颖而不端谨”，予以除名。
              [6]
              但邹容毫不气馁，怀着渴望摆脱传统桎梏，探索新知的心情，在父亲面前犯颜力争，要求自费留日。当年深秋，他从亲戚处借得路费，乘船发自重庆，穿过三峡，顺流东下，到达上海。不久，进入江南制造局附设的广方言馆补习日语。邹子璠旋经亲戚劝说，答应支付留日费用。
              
            </p>
            <p>光绪二十八年（1902年）春，他冲破重重阻力，自费东渡日本，进入东京同文书院。
              [7]
              到日本之后，邹容刻苦读书，他阅读了大量的西方资产阶级启蒙著作，曾“自比法国卢梭”，同时他还广泛的参加留日学生的各种活动，每到集会必参加演讲。在日本，邹容大量接触西方资产阶级民主思想与文化，革命倾向日趋显露，并结识了一些革命志士，积极参加留日学生的爱国活动。他刚毅勇为，常争先讲演，陈述己见，其辞犀利悲壮，为公认的革命分子。当时清政府派驻日本的留日陆军学生监督姚文甫道德败坏，勾引留日学生监督钱某的小老婆。邹容等几个留学生拿着剪刀闯进姚文甫的住所，一见面先打了他几个耳光，邹容又把他的辫子剪了。事后，姚文甫向清政府驻日公使蔡钧控告邹容。邹容的狂放悖逆行为引起了清廷的极大震怒，在清廷和日本方面的双重压力下，邹容被迫离日回国。</p>
          </div>
          <div>
            <h3>革命活动</h3>
            <p>
              光绪二十九年（1903年）三月，传闻广西巡抚王之春将借法兵平定该省会党起事，东京留日学生即致电爱国学社及中国教育会，盼相互响应，亟起抵制。二十八日（4月25日），上海爱国人士在张园开“拒法”大会。不旋踵，又传来俄国违约不履行第二期撤兵协议，妄图独占东三省权利的消息。上海人士又于四月初一日（4月27日）会于张园，通电“拒俄”。在狂飙乍起的拒俄运动中，邹容积极参与张园集会和拒俄义勇队的操练。四月二十八日（5月24日），中国教育会在张园开四月月会，邹容作《论改革中国现时大势》的演说，爱国热望，意切情深。同时，他还提出组织“中国学生同盟会”的倡议，号召全国学生结成大团体。邹容迫切地期望新兴的、最有觉悟的知识分子，乘时振作，肩负起反清民主革命的历史任务。正是在这种爱国激情驱使下，邹容写完了《革命军》一书，署名“革命军中马前卒邹容”，持请章太炎修改。章看过后，击节赞赏，认为正是这样直率豪放、通俗的文字，能够发挥广泛的宣传作用，因而不做任何修饰，提笔书写一篇序文，称许《革命军》是“义师先声”。随即由柳亚子等筹集印刷费用，交大同书局排印，于五月初出版发行。
            </p>
            <p>
              邹容以西方资产阶级革命时期提出的“天赋人权”、“自由、平等、博爱”为指导思想，阐述了反对封建专制、进行资产阶级民主革命的必要性，指出了“革命”乃对上下古今、宗教、道德、政治、学术，以及日常事物存善去恶、存美去丑、存良善而除腐败的过程，故赞美曰：“巍巍哉！革命也。皇皇哉！革命也。”他还从清王朝官制的腐败、刑审、官吏的贪酷，对知识分子、对农民、对海外华工、对商人、对士兵的政策及对外的一系列政策，揭露了清政府对国人的压迫，分析了革命爆发的必然性。明确宣布革命独立之大义在于：“永脱满洲之羁绊，尽复所失之权利，而介于地球强国之间”，“全我天赋平等自由之位置”，“保我独立之大权”，即推翻封建专制王朝，建立“中华共和国”！
            </p>
          </div>
          <div>
            <h3>入狱牺牲</h3>
            <p>
              正当《革命军》问世之际，《苏报》因章士钊接充主笔，于五月初六日（6月1日）宣布“大改良”，言论更趋向激进，“排满”、“仇满”的文词，连篇迭出。初六日刊登《康有为》一文，痛斥康有为力倡保皇、抵拒革命的言行。十四日（9日），刊登《读〈革命军〉》，将该书誉为“诚今日国民教育之第一教科书”；又在“新书介绍”栏评论说，《革命军》“其宗旨专在驱除满族，光复中国，笔极犀利，文极沉痛。若能以此书普及四万万人之脑海，中国当兴也勃焉”。闰五月初五日（6月29日）以《康有为与觉罗君之关系》为题摘登章太炎《驳康有为书》，指斥“载湉小丑，未辨菽麦”，对康有为的保皇谬论痛加批驳。《苏报》迅即成为举国瞩目，独步一时的革命报刊。闰五月初五日、初六日，《苏报》馆帐房程吉甫和章太炎以及陈范的儿子陈仲彝、办事员钱宝仁等被捕。初七日（7月1日），邹容自投捕房，以与章太炎共患难。蔡元培事前离沪赴青岛，陈范、黄宗仰、吴稚晖等避匿，旋出国远走。随后，领事团徇上海道的请求，于十三日将《苏报》封闭。
            </p>
            <p>
              邹容自入狱起，即抱定为革命牺牲的决心。他同章太炎在狱中赋诗明志，互相砥砺。闰五月二十八日，章吟《狱中赠邹容》一首：“邹容吾小弟，被发下瀛洲。快剪刀除辫，干牛肉作糇。英雄一入狱，天地亦悲秋。临命宜掺手，乾坤只两头。”邹容即赋《狱中答西狩》相唱和：“我兄章枚叔，忧国心如焚。并世无知己，吾生苦不文。一朝沦地狱，何日扫妖氛！昨夜梦和尔，同兴革命军。”充分表现了革命者临危不惧，勇于献身，对前途充满信心的高尚情操和英雄气概。
            </p>
            <p>
              不幸的是，邹容距出狱只有两个多月，即被折磨致病，于光绪三十一年二月二十九日（1905年4月3日）在狱中逝世，年仅20岁。噩耗传出，中国教育会立即为他开追悼会。遗骸由革命志士刘三（季平）冒险运出，安葬于上海华泾乡。
              [8]
              南京临时政府成立后，经临时大总统孙中山批示：邹容“照陆军大将军阵亡例赐恤”。
              [9]
            </p>
