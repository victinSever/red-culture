        <div>
          <p>
            这是一篇白话述志诗，分为上下两节。全诗明白晓畅，通俗易懂，不着一典
            ，没有华丽的文字雕饰，更没有用一个典故，但音韵嘹亮，感情炽烈，气势豪迈，意境壮美，让人读后被作者那股凛然正气所震撼。这是诗人高尚情操的真实流露，让读者情不自禁地为诗中的那股凛然正气而震撼，为诗人的高尚人格而肃然起敬。
          </p>
          <p>
            诗的上半节，作者以“人”与“狗”、“门”和“洞”作对比，鲜明的阐述了革命者对于人的气节的崇高追求。匈牙利诗人裴多菲曾有过“生命诚可贵，爱情价更高，若为自由故，二者皆可抛。”之名句，表达出了人对自由的执著追求。国民党反动派也正是想利用人的这种本能的渴望，来征服他们用酷刑所无法征服的革命者。然而，他们又怎会知道，他们所给予的“自由”和革命者渴望的“自由”之间有着何等的差别？这种诱惑在像诗人这样坚贞的革命者面前，起不了任何作用。革命者正是因为要为更多的人能得到真正的自由，才甘愿抛头颅，洒热血，即使是身陷牢狱，也义无反顾。虽然失去了人身的自由，但如果失去这种“小自由”而能换来更多人的“大自由”，那有什么可以顾惜的呢！古人说：“烈士之所以异于恒人，以其仗节以配谊也。”而诗人正是这样一位富贵不淫，贫贱不移，威武不屈的堂堂正正的革命志士。他写道：“我渴望自由，但我深深地知道——人的身躯怎能从狗洞子里爬出！”所以当他面临着“自由”和“囚禁”的选择时，为了保持一个“人”的尊严，为了保持一个共产党人的革命气节，他宁可选择“囚禁”。
            诗人伟大的人格在这里得到了充分地体现。
          </p>
          <p>
            诗的下半节，诗人以激昂的语气来回答了那个在外面高叫的声音，告诉了他们自己的选择，用激烈的话语表达了宁为玉碎，不为瓦全的高尚志向和无畏的气概。在诗中诗人用“活棺材”来比喻这座囚禁革命志士的人间炼狱，一方面，揭露了国民党反动派在这里残酷迫害革命志士，屠杀共产党人的暴行；另一方面，也以此来揭示国民党反动统治必将走向灭亡的最终结局。诗人坚信革命终会取得最后的胜利，他盼望着革命的烈火熊熊燃起，烧毁这人间的炼狱。他愿为国家、为民族献出自己的热血和生命，让自己在血与火的斗争中体现生命的真正价值，而得到永生。我们仿佛看到，在熊熊燃烧的烈火中，一个顶天立地的共产党人的高大形象巍然屹立起来，光耀千秋。
          </p>
          <p>
            本诗是革命者用鲜血和生命谱写成的雄壮乐章，明白晓畅，通俗易懂，没有华丽的文字雕饰，更没有用一个典故，但音韵嘹亮，感情炽烈，气势豪迈，意境壮美，让人读后被作者那股凛然正气所震撼。作者遣词造句极其精炼形象，以“人”与“狗”、“门”和“洞”作对比，表达了自己在面临自由和囚禁的选择时，为了捍卫人的尊严，为了保持共产党人的气节，宁可选择囚禁的决绝态度和牺牲精神。作者的伟大人格在这里得到“诗性”的升华。生动的比喻也是此诗脍炙人口的原因之，诗人用“活棺材”比喻那些囚禁革命志士的牢房和反动派的统治，又用“地下的火”比喻广大人民的反抗斗争。诗人盼望着革命的烈火熊熊燃起，烧毁这人间的炼狱，并且希望能实现自身生命价值的最大化——在烈火和热血中得到永生。本诗所体现出的崇高革命气节和伟大爱国精神，将永垂青史，激励和教育着成千上万的革命后代。
          </p>
